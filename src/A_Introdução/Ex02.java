public class Ex02 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        float a = 3;
        float b = 4;
        
        float res = a + b;
        System.out.println(a + " + " + b + " = " + res);
        
        res = a - b;
        System.out.println(a + " - " + b + " = " + res);
        
        res = a * b;
        System.out.println(a + " x " + b + " = " + res);
        
        res = a / b;
        System.out.println(a + " / " + b + " = " + res);
    }
    
}
